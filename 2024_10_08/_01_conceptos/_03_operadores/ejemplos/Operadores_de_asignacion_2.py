bits = 179
#print(bits)

bits |= 0xb10101 # Es equivalente a la siguiente asignación: bits = bits | 0xb10101
#print(bits)

bits &= 0xb10101 # Es equivalente a la siguiente asignación: bits = bits & 0xb10101
#print(bits)

bits ^= 0xb11 # Es equivalente a la siguiente asignación: bits = bits ^ 0xb11
#print(bits)

bits <<= 2 # Es equivalente a la siguiente asignación: bits = bits << 2
#print(bits)

bits >>= 2 # Es equivalente a la siguiente asignación: bits = bits >> 2
#print(bits)
