# ------------------------------------------------------------------------------------------
# Ejemplos de operadores de asignación
# ------------------------------------------------------------------------------------------

valor = 45 # Asignamos el valor 45 a la variable denominada 'valor'
#print(valor)

valor += 1 # Es equivalente a la siguiente asignación: valor = valor + 1
#print(valor)

valor -= 1 # Es equivalente a la siguiente asignación: valor = valor - 1
#print(valor)

valor *= 10 # Es equivalente a la siguiente asignación: valor = valor * 10
#print(valor)

valor /= 10 # Es equivalente a la siguiente asignación: valor = valor / 10
#print(valor)

valor //= 10 # Es equivalente a la siguiente asignación: valor = valor // 10
#print(valor)

valor %= 10 # Es equivalente a la siguiente asignación: valor = valor % 10
#print(valor)

valor **= 2 # Es equivalente a la siguiente asignación: valor = valor ** 2
#print(valor)

