# Operador de asignación    Equivale a                  Descripción
# ------------------------------------------------------------------------------------------
#       =                                               Asigna un valor

# Operación de asignación:
# Se asigna el valor u operación aritmética que hay a la derecha después del símbolo igual
# a la variable que está antes del símbolo igual

edad = 17 # Asignamos el valor 17 a la variable 'edad'
print(edad)

area_circulo = 3.14 * 2.4 ** 2.0 # Asignamos el resultado de la operación aritmética
                                 # a la variable 'area_circulo'
print(area_circulo)

