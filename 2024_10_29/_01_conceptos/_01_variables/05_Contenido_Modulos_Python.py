# Definición de módulo Python
# ---------------------------------------------------------------------------------------------
"""
Es un archivo de texto que contiene el código fuente escrito en
el lenguaje de programación Python.
El nombre del archivo debe de tener la extensión .py (aunque la
extensión del nombre de archivo no es necesariamente obligatoria).

---------------------------------------------------------------------------------------------
Contenido de un módulo de Python:
---------------------------------------------------------------------------------------------

Los módulos pueden contener el siguiente código fuente en Python:

- Definición de sentencias
    * De asignación
    * Condicionales o de bifurcación
    * Repetitivas o bucles
    * Llamadas a funciones
- Definición de funciones
- Definición de clases
- Una combinación de todo lo anterior

---------------------------------------------------------------------------------------------
Ejecución de un módulo de Python:
---------------------------------------------------------------------------------------------
El código fuente escrito en un módulo de Python se ejecuta siempre de arriba hacia abajo,
es decir, desde la primera línea escrita hasta la última línea del módulo.
La ejecución de un módulo es lineal y en sentido vertical.
"""
